//unary digunakan untuk operasi 1 data saja

let result = +1; // nilai default sebenarnya positif jadi bisa saja ditulis 1
document.writeln("<p>" + result + "</p>")

result++;//menaikan nilai 1 angka, bisa juga ditulis result=result+1 atau result +=1
document.writeln("<p>" + result + "</p>")

result--;//menurunkan nilai 1 angka, bisa juga ditulis result=result-1 atau result -=1
document.writeln("<p>" + result + "</p>")

result = -result;// mengubah nilai menjadi negative
document.writeln("<p>" + result + "</p>")
