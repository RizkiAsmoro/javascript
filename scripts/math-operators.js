/*
Operator Aritmatika :
+ untuk penjumlahan
- untuk pengurangan
* untuk perkalian
** untuk Exponensial (pangkat)
/ untuk pembagian
% untuk modulo(sisa bagi)
*/

let result = 1 + 3;
document.writeln("<p> 1 + 3 = " + result + "</p>");
let originalResult = result;

result = result * 2;
document.writeln("<p>" + originalResult + " x 2 = " + result +"</p>");
originalResult = result;

result = result - 3
document.writeln("<p>" + originalResult + " - 3 = " + result +"</p>")